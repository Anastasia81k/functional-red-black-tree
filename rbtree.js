//Find the ith item in the tree
proto.at = function(idx) {
  var n = this.root
  var stack = []
  while(n) {
    stack.push(n)
    if(n.left) {
      if(idx < n.left._count) {
        n = n.left
        continue
      }
      idx -= n.left._count
    }
    if(!idx) {
      return new RedBlackTreeIterator(this, stack)
    }
    if(n.right) {
      idx -= 1
      if(idx >= n.right._count) {
        break
      }
      n = n.right
    }
  }
  return new RedBlackTreeIterator(this, [])
}

